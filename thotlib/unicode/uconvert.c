/*----------------------------------------------------------------------
  TtaWCToMBstring converts a wide character into a multibyte string.
  Returns the number of bytes in the multibyte character or -1
  The pointer to the dest multibyte string is updated.
  ----------------------------------------------------------------------*/
int TtaWCToMBstring (wchar_t src, unsigned char **dest)
{
  unsigned char   leadByteMark;
  unsigned char  *mbcptr = *dest;
  int             nbBytes = 1;

  if (src < 0x80)
    {
      nbBytes  = 1;
      leadByteMark = 0x00;
    }
  else if (src < 0x800)
    {
      nbBytes      = 2;
      leadByteMark = 0xC0;
    }
  else if (src < 0x10000)
    {
      nbBytes      = 3;
      leadByteMark = 0xE0;
    }
  else if (src < 0x200000)
    {
      nbBytes      = 4;
      leadByteMark = 0xF0;
    }
  else if (src < 0x4000000)
    {
      nbBytes      = 5;
      leadByteMark = 0xF8;
    }
  else if (src < 0x7FFFFFFF)
    {
      nbBytes      = 6;
      leadByteMark = 0xFC;
    }
  else
    {
      *mbcptr = '?';
      return -1;
    }

  mbcptr += nbBytes;
  *dest = mbcptr;
  switch (nbBytes)
    {
    case 6:
      *--mbcptr = (src | 0x80) & 0xBF;
      src >>= 6;
    case 5:
      *--mbcptr = (src | 0x80) & 0xBF;
      src >>= 6;
    case 4:
      *--mbcptr = (src | 0x80) & 0xBF;
      src >>= 6;
    case 3:
      *--mbcptr = (src | 0x80) & 0xBF;
      src >>= 6;
    case 2:
      *--mbcptr = (src | 0x80) & 0xBF;
      src >>= 6;
    case 1:
      *--mbcptr = src | leadByteMark;
    }
  return nbBytes;
}


/*----------------------------------------------------------------------
  TtaMBstringToWCS converts a multibyte string into a wide character.
  Returns the number of bytes in the multibyte character or -1
  The pointer to the source multibyte string is updated.
  ----------------------------------------------------------------------*/
int TtaMBstringToWC (unsigned char **src, wchar_t *dest)
{
  unsigned char *ptrSrc = *src;
  wchar_t        res;
  int            nbBytesConverted = 0;
  int            nbBytesToConvert;

  if (*ptrSrc < 0xC0)
    nbBytesToConvert = 1;
  else if (*ptrSrc < 0xE0)
    nbBytesToConvert = 2;
  else if (*ptrSrc < 0xF0)
    nbBytesToConvert = 3;
  else if (*ptrSrc < 0xF8)
    nbBytesToConvert = 4;
  else if (*ptrSrc < 0xFC)
    nbBytesToConvert = 5;
  else if (*ptrSrc <= 0xFF)
    nbBytesToConvert = 6;
                 
  nbBytesConverted += nbBytesToConvert;
  res = 0;
  switch (nbBytesToConvert)
    {
    case 6:
      res += *ptrSrc++;
      res <<= 6;
    case 5:
      res += *ptrSrc++;
      res <<= 6;
    case 4:
      res += *ptrSrc++;
      res <<= 6;
    case 3:
      res += *ptrSrc++;
      res <<= 6;
    case 2:
      res += *ptrSrc++;
      res <<= 6;
    case 1:
      res += *ptrSrc++;
    }

  res -= offset[nbBytesToConvert - 1];
  if (res <= 0xFFFF)
    *dest = res;
  else 
    *dest = '?';
  *src = ptrSrc;
  if (nbBytesConverted > 0)
    return nbBytesConverted;
  else
    return -1;
}


/*----------------------------------------------------------------------
  TtaGetNextWCFromString: Looks for the next Wide character 
  value in a multibyte character string.
  ----------------------------------------------------------------------*/
int TtaGetNextWCFromString (wchar_t *car, unsigned char **txt, CHARSET encoding)
{
  int            nbBytesToRead;
  unsigned char *start = *txt;
  wchar_t        res;

  if (encoding ==  UTF_8)
    {
      if (*start < 0xC0)
	nbBytesToRead = 1;
      else if (*start < 0xE0)
	nbBytesToRead = 2;
      else if (*start < 0xF0)
	nbBytesToRead = 3;
      else if (*start < 0xF8)
	nbBytesToRead = 4;
      else if (*start < 0xFC)
	nbBytesToRead = 5;
      else if (*start <= 0xFF)
	nbBytesToRead = 6;
      
      res = 0;
      /* See how many bytes to read to build a wide character */
      switch (nbBytesToRead)
	{
	  /** WARNING: There is not break statement between cases */
	case 6:
	  res += *start++;
	  res <<= 6;
	case 5:
	  res += *start++;
	  res <<= 6;
	case 4:
	  res += *start++;
	  res <<= 6;
	case 3:
	  res += *start++;
	  res <<= 6;
	case 2:
	  res += *start++;
	  res <<= 6;
	case 1:
	  res += *start++;
	}
      res -= offset[nbBytesToRead - 1];      
      *car = res;
    }
  else
    {
      nbBytesToRead = 1;
      *car = TtaGetWCFromChar (*start, encoding);
      start++;
    }
  return nbBytesToRead;
}

